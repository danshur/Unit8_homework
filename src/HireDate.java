
public class HireDate {

	private int hireDay;
    private int hireMonth;
    private int hireYear;

    public HireDate(int day, int month, int year) {
    	this.hireDay = day;
    	this.hireMonth = month;
    	this.hireYear = year;
    }
    
    public int getHireDay() {
        return hireDay;
    }

    public int getHireMonth() {
        return hireMonth;
    }

    public int getHireYear() {
        return hireYear;
    }
    
}
